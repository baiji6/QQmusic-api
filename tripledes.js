function f(state, key) {
  const t1 =
    ((state & 1) << 31) |
    ((state & 0xf8000000) >>> 1) |
    ((state & 0x1f800000) >>> 3) |
    ((state & 0x01f80000) >>> 5) |
    ((state & 0x001f8000) >>> 7);
  const t2 =
    ((state & 0x0001f800) << 15) |
    ((state & 0x00001f80) << 13) |
    ((state & 0x000001f8) << 11) |
    ((state & 0x0000001f) << 9) |
    ((state & 0x80000000) >>> 23);

  const k0 = (((t1 >>> 24) & 0xff) ^ key[0]) & 0xff;
  const k1 = (((t1 >>> 16) & 0xff) ^ key[1]) & 0xff;
  const k2 = (((t1 >>> 8) & 0xff) ^ key[2]) & 0xff;
  const k3 = (((t2 >>> 24) & 0xff) ^ key[3]) & 0xff;
  const k4 = (((t2 >>> 16) & 0xff) ^ key[4]) & 0xff;
  const k5 = (((t2 >>> 8) & 0xff) ^ key[5]) & 0xff;

  let st =
    (SBOX[0][sboxBit(k0 >> 2)] << 28) |
    (SBOX[1][sboxBit(((k0 & 0x03) << 4) | (k1 >> 4))] << 24) |
    (SBOX[2][sboxBit(((k1 & 0x0f) << 2) | (k2 >> 6))] << 20) |
    (SBOX[3][sboxBit(k2 & 0x3f)] << 16) |
    (SBOX[4][sboxBit(k3 >> 2)] << 12) |
    (SBOX[5][sboxBit(((k3 & 0x03) << 4) | (k4 >> 4))] << 8) |
    (SBOX[6][sboxBit(((k4 & 0x0f) << 2) | (k5 >> 6))] << 4) |
    SBOX[7][sboxBit(k5 & 0x3f)];

  st =
    (((st >> 16) & 1) << 31) |
    (((st >> 25) & 1) << 30) |
    (((st >> 12) & 1) << 29) |
    (((st >> 11) & 1) << 28) |
    (((st >> 3) & 1) << 27) |
    (((st >> 20) & 1) << 26) |
    (((st >> 4) & 1) << 25) |
    (((st >> 15) & 1) << 24) |
    (((st >> 31) & 1) << 23) |
    (((st >> 17) & 1) << 22) |
    (((st >> 9) & 1) << 21) |
    (((st >> 6) & 1) << 20) |
    (((st >> 27) & 1) << 19) |
    (((st >> 14) & 1) << 18) |
    (((st >> 1) & 1) << 17) |
    (((st >> 22) & 1) << 16) |
    (((st >> 30) & 1) << 15) |
    (((st >> 24) & 1) << 14) |
    (((st >> 8) & 1) << 13) |
    (((st >> 18) & 1) << 12) |
    (((st >> 0) & 1) << 11) |
    (((st >> 5) & 1) << 10) |
    (((st >> 29) & 1) << 9) |
    (((st >> 23) & 1) << 8) |
    (((st >> 13) & 1) << 7) |
    (((st >> 19) & 1) << 6) |
    (((st >> 2) & 1) << 5) |
    (((st >> 26) & 1) << 4) |
    (((st >> 10) & 1) << 3) |
    (((st >> 21) & 1) << 2) |
    (((st >> 28) & 1) << 1) |
    (((st >> 7) & 1) << 0);
  return st >>> 0;
}

function crypt(input, key) {
  let [s0, s1] = initialPermutation(input);
  for (let idx = 0; idx < 15; idx++) {
    const prev = s1;
    s1 = (f(s1, key[idx]) ^ s0) >>> 0;
    s0 = prev;
  }
  s0 = (f(s1, key[15]) ^ s0) >>> 0;
  return inversePermutation(s0, s1);
}

function keySchedule(key, mode) {
  const schedule = Array.from({ length: 16 }, () => new Array(6).fill(0));
  const keyRndShift = [1, 1, 2, 2, 2, 2, 2, 2, 1, 2, 2, 2, 2, 2, 2, 1];
  const keyPermC = [
    56, 48, 40, 32, 24, 16, 8, 0, 57, 49, 41, 33, 25, 17, 9, 1,
    58, 50, 42, 34, 26, 18, 10, 2, 59, 51, 43, 35,
  ];
  const keyPermD = [
    62, 54, 46, 38, 30, 22, 14, 6, 61, 53, 45, 37, 29, 21, 13, 5,
    60, 52, 44, 36, 28, 20, 12, 4, 27, 19, 11, 3,
  ];
  const keyCompression = [
    13, 16, 10, 23, 0, 4, 2, 27, 14, 5, 20, 9, 22, 18, 11, 3,
    25, 7, 15, 6, 26, 19, 12, 1, 40, 51, 30, 36, 46, 54, 29, 39,
    50, 44, 32, 47, 43, 48, 38, 55, 33, 52, 45, 41, 49, 35, 28, 31,
  ];

  const v0 = (key[0] | (key[1] << 8) | (key[2] << 16) | (key[3] << 24)) >>> 0;
  const v1 = (key[4] | (key[5] << 8) | (key[6] << 16) | (key[7] << 24)) >>> 0;

  let c = 0;
  for (let i = 0; i < keyPermC.length; i++) {
    const b = keyPermC[i];
    const bit = b < 32 ? (v0 >>> (31 - b)) & 1 : (v1 >>> (63 - b)) & 1;
    c |= bit << (31 - i);
  }
  let d = 0;
  for (let i = 0; i < keyPermD.length; i++) {
    const b = keyPermD[i];
    const bit = b < 32 ? (v0 >>> (31 - b)) & 1 : (v1 >>> (63 - b)) & 1;
    d |= bit << (31 - i);
  }
  c = c >>> 0;
  d = d >>> 0;

  for (let i = 0; i < 16; i++) {
    const sft = keyRndShift[i];
    c = (((c << sft) | (c >>> (28 - sft))) & 0xfffffff0) >>> 0;
    d = (((d << sft) | (d >>> (28 - sft))) & 0xfffffff0) >>> 0;
    const togen = mode === DECRYPT ? 15 - i : i;

    for (let j = 0; j < 6; j++) schedule[togen][j] = 0;

    for (let j = 0; j < 24; j++) {
      const bit = (c >>> (31 - keyCompression[j])) & 1;
      schedule[togen][j >> 3] |= bit << (7 - (j & 7));
    }
    for (let j = 24; j < 48; j++) {
      const bit = (d >>> (31 - (keyCompression[j] - 27))) & 1;
      schedule[togen][j >> 3] |= bit << (7 - (j & 7));
    }
  }
  return schedule;
}

export function tripledesKeySetup(key, mode) {
  if (mode === ENCRYPT) {
    return [keySchedule(key.subarray(0, 8), ENCRYPT), keySchedule(key.subarray(8, 16), DECRYPT), keySchedule(key.subarray(16, 24), ENCRYPT)];
  }
  return [keySchedule(key.subarray(16, 24), DECRYPT), keySchedule(key.subarray(8, 16), ENCRYPT), keySchedule(key.subarray(0, 8), DECRYPT)];
}

export function tripledesCrypt(data, key) {
  let buf = new Uint8Array(data);
  for (let i = 0; i < 3; i++) {
    buf = crypt(buf, key[i]);
  }
  return buf;
}
